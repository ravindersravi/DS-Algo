package AAtutorialPoint;

public class Add2Strings {
    public static void main(String[] args) {
        String a = "111";
        String b = "222";
        Integer a1 = Integer.parseInt(a);
        Integer b1 = Integer.parseInt(b);
        Integer c = a1+b1;
        String s = String.valueOf(c);
        System.out.println(s);
    }
}
